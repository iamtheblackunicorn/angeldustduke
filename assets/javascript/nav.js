function openNav(){
  var navElement = document.getElementById('nav');
  navElement.style.width = '100vw';
  navElement.style.height = '100vh';
}
function closeNav(){
  var navElement = document.getElementById('nav');
  navElement.style.width = '0vw';
  navElement.style.height = '0vh';
}
